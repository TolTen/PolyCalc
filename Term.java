

public class Term {
    int coefficient;
    int degreeX;
    int degreeY;
    int degreeZ;

    public Term(int coefficient, int degreeX, int degreeY, int degreeZ) {
        this.coefficient = coefficient;
        this.degreeX = degreeX;
        this.degreeY = degreeY;
        this.degreeZ = degreeZ;
    }


}
